    <h4 id="ota">3.5 Observando el estado de trámite en usuario</h4>
    <div> 
        <center>3.5.a Trámite en usuario</center>
        <ul><li>Al entrar a Trámite en Usuario y si se aprobado la solicitud en archivo, aparecera una lista con las solicitudes que se aprobaron en archivo.Para recibir la solicitud haga clic en el icono de <b>Recibir</b>.</ul>
        
        <center>3.5.b Historial del Trámite</center>
        <ul><b>Nota:</b><br>Puede visualizar el proceso de atención haciendo clic en icono de archivo. Aparecerá una ventana con la información de la atención.</li></ul>
        
        <center>3.5.c Recepcionado la solicitud</center>
        <ul><li>Una vez agregue alguna observación adicional a la solicitud, procedera a hace clic en una de las tres opciones disponibles.
                <br> La primera <b>Atras</b>, le regresara a la pantalla anterior. La segunda <b>Recibir</b>, sirve para notificar que el proceso de recepción ha sido exitoso. Finalmente la tercera, <b>Perdida</b>, le permitira notificar algun problema en el proceso de recepción del físico del documento.</ul>
        
