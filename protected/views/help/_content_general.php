    <h4 id="sda">3.2 Solicitando documentos con archivos</h4>
    <div class="manualbody">
        <center>3.2.a Eligiendo la opción 'Cargar Solicitud'</center>
        <ul><li>Para iniciar haga clic en 'Cargar Solicitud'.</li></ul>
        <center>3.2.b Seleccione un archivo para subir</center>
        <ul><li>En la pantalla que aparece haga clic en <b>Examinar</b> para ubicar el archivo que contiene los parámetros de búsqueda.Una vez localizado el archivo haga clic en <b>Subir Archivo</b>.</li></ul>
        <ul><b>Nota:</b><br>El archivo debe de ser <b>.csv</b> y contener el formato de subida. Para ver el formato haga clic en <b>Ver encabezado de excel</b>.</ul>
        <center>3.2.c La búsqueda no encontro ningún documento</center>
        <ul><li>Si no encontró nada en la búsqueda le mostrara una pantalla con el mensaje 'no hay datos'.</li></ul>
        <center>3.2.d Se encontraron documentos</center>
        <ul><li>Si la búsqueda contiene resultados aparecerá un mensaje de afirmación con el número de resultados.</li></ul>
        <center>3.2.e Lista con los documentos encontrados</center>
        <ul><li>Se mostrará los elementos encontrados y dos opciones 'Agregar otros documentos' y 'Solicitar servicio'.</li></ul>
        <ul><li>La opción <b>Agregar otros documentos</b> permitirá ir a la pantalla de solicitar para agregar otros documentos que se necesiten o que no se halla encontrado en la búsqueda.</li></ul>
        <center>3.2.g Detalles del servicio encontrado por archivo</center>
        <ul><li>La opción <b>Solicitar Servicio</b> permitirá ir a la pantalla de Detalle de la solicitud y confirmar la solicitud. Para finalizar complete el tipo del servicio que desea, el motivo de la solicitud y haga clic en <b>Enviar Solicitud</b>.</li></ul>
    </div>
    <div style="text-align: right">[ <a href="#Top">Subir</a> ]</div>
    <h4 id="uoe">3.3 Utilizando la opción 'no se encontró'</h4>
    <div> 
        <center>3.3.a Clic en 'No se Encontró'</center>
        <ul><li>Para iniciar haga clic en 'Solicitar'->'No se Encontró' en la pantalla de la opción .</li></ul>
        <center>3.3.b Formulario a completar</center>
        <ul><li>Aquí se mostrará una pantalla para especificar la solicitud en caso de no encontrarla con el buscador.
                <br>Complete las casillas en blanco con los respectivo valores de los documentos a solicitar. Una vez ingresado los valores del documento a solicitar proceda con especificar los datos de la solicitud. Para finalizar haga clic en <b>Enviar Solicitud</b>.</ul>
        <ul><b>Nota:</b><br>En caso de introducir valores que sean erróneos o que hagan falta para especificar la solicitud, un mensaje les advertira de los elementos que se necesitan corregir/completar en la solicitud.</li></ul>
    </div>
    <div style="text-align: right">[ <a href="#Top">Subir</a> ]</div>
    <h4 id="uea">3.4 Usando 'no se encontró' con archivos</h4>
    <div> 
        <center>3.4.a Registrando una solicitud con anexo</center>
        <ul><li>Para iniciar debe de ubicarse en la opción no se encontró. Seleccionar un tipo de serie de documento. Al elegir el tipo de doc. seleccione <b>>> Servicio con hoja Anexa</b></li></ul>
        <ul><li>Deberá localizar el archivo excel donde usted a especificado los detalles de la solicitud.(EJ: tipo documento,tipo de servicio,caja,paquete,nro documento de los documentos solicitados)
                <br>Luego hacer clic en la opción <b>Cargar</b>.</li></ul>
        <ul><li>Para finalizar complete el tipo del servicio que desea, el motivo de la solicitud y haga clic en <b>Enviar Solicitud</b>.</li></ul>
        <center>3.4.b Archivo de prueba cargado a la solicitud</center>
        <ul><b>Nota:</b><br> Para verificar si el archivo se subio correctamente haga clic en <b>Ver Archivo</b>.</li></ul>
    </div>
