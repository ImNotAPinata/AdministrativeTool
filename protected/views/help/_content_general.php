    <h3 id="UsandoSistema">3. Usando el Sistema</h3>
    <ul>
    <h4 id="sma">3.1 Solicitando documentos</h4>
    <div>
        <center>3.1.a Solicitar</center>
        <ul><li>Para iniciar haga clic en 'Solicitar'.</li></ul>
        <center>3.1.b Buscar documento</center>
        <ul><li>Aquí se mostrará una lista de documentos para su respectiva selección.
                <br> Para filtrar estos documentos sirvase llenar las casillas en blanco con el respectivo valor de búsqueda. (EJ: Nro Lote: 325, Razón Social: "Empresa XYZ S.A.C."). Una vez ingresado los valores de búsqueda haga clic en <b>Buscar</b>.
                <br> Si existen documentos con los criterios de búsqueda ingresados, estos se mostraran para que usted fácilmente pueda seleccionarlos y elegir.</li></ul>
        <ul><b>Importante:</b><br>Algunos documentos (EJ:Comprobantes de pago, otros) estan registrados por fechas por lo que la búsqueda se debera relizarse indicando la fecha.</ul>
        <ul><b>Nota:</b><br>Si encuentra algún valor que no debería corresponder o esta mal grabado en el sistema (EJ: '064-2-07500'->Debería ser->'064207500') por favor comunicarlo al administrador del sistema para su respectiva correción.</ul>
        <center>3.1.c Agregar documento</center>
        <ul><li>Eliga los documentos que deseé agregar a la solicitud.Para seleccionarlos haga clic en las casillas a lado de los documentos y eliga el que este de acuerdo a su criterio. Ahora para agregar a la lista de documentos que va a solicitar haga clic en <b>Agregar a Solicitud</b>. 
                <br>Puede iniciar otra búsqueda e ir agregando otros archivos a la lista de documentos que desea solicitar.</li></ul>
        <center>3.1.d Lista con los documento solicitados</center>
        <ul><li>Una vez hecho de la lista de los documentos que desea solicitar se debe de proseguir a solicitarlos. Para ello haga clic en <b>Solicitar Servicio</b>.</li></ul>
        <ul><b>Nota:</b><br>En caso de que deseé retirar algun documento de su lista de documentos haga clic en <b>SUPR</b> sobre el elemento que desea retirar.</ul>
        <center>3.1.e Nro de Documento no encontrado</center>
        <ul><b>Nota:</b><br>La razón por la que muchos de los documentos no se encuentran es debido a que no estan registrados en la base de datos o a que estan registrados mal.(sin el 0 inicial o diferentes formatos tales como 'xxx-xxx-xxxx'). En caso de encontrar algún nro de documento mal registrado o faltante, deberá tomar un pantallazo y/o notificarlo al administrador del sistema indicando la forma correcta y/o la descripción del documento para su registro. </ul>
        <center>3.1.f Detalles de la Solicitud </center>
        <ul><li>Al solicitar el servicio le aparecera una nueva pantalla en la cual podrá revizar la información general de los documentos que esta por solicitar.</li></ul>
        <ul><b>Nota:</b><br>Si usted ingreso el nro documento para la búsqueda o la búsqueda lo realizó por medio de un archivo, los valores buscados apareceran automáticamente en las descripciones de los documentos solicitados.</ul>
        <center>3.1.g Completando los datos de la solicitud</center>
        <ul><li>Para finalizar complete el tipo del servicio que desea, el motivo de la solicitud y haga clic en <b>Enviar Solicitud</b>.</li></ul>
        <ul><b>Importante:</b><br>Asegúrese de que el documento solicitando corresponda a lo que solicita; puede existir documentos de diferentes tipos pero relacionados con el nro documento buscado.</ul>
    </div>
